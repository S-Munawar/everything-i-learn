# 2. Assignment Operators
# Used to assign values to variables.

print("--- Assignment Operators ---")
x = 5
print(f"Assignment (=): x = {x}")

x += 3
print(f"Add and Assign (+=): x += 3 -> x is now {x}")

x -= 2
print(f"Subtract and Assign (-=): x -= 2 -> x is now {x}")

x *= 4
print(f"Multiply and Assign (*=): x *= 4 -> x is now {x}")

x /= 3
print(f"Divide and Assign (/=): x /= 3 -> x is now {x}")
