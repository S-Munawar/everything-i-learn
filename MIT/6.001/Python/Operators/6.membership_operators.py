# 6. Membership Operators
# Used to test if a sequence is presented in an object.

print("--- Membership Operators ---")
name = "Bro Code"
fruits = ["apple", "banana", "cherry"]

print(f"in string: 'Bro' in '{name}' is {'Bro' in name}")
print(f"not in string: 'z' not in '{name}' is {'z' not in name}")

print(f"in list: 'apple' in fruits is {'apple' in fruits}")
print(f"not in list: 'orange' not in fruits is {'orange' not in fruits}")
